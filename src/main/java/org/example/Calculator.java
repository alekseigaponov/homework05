package org.example;

public class Calculator {

    public int calculateSumOfTwoIntegerValues (int firstValue, int secondValue) {

        return firstValue + secondValue;

    }
    // HW6.
    // The method of dividing floating point numbers
    public double calculateDivisionOfTwoDoubleNumbers(double firstValue, double secondValue){
        return firstValue / secondValue;
    }
    // The method of multiplying floating point numbers
    public double calculateMultiplicationOfTwoDoubleNumbers(double firstValue, double secondValue) {
        return firstValue * secondValue;
    }
}
